/* ******************************** LIBRARIES ******************************* */

#include "ft_printf.h"

/* ******************************** FUNCTIONS ******************************* */

char	*ft_itov(unsigned long int n)
{
	char	*str;
	char	*hex;
	int		i;

	str = ft_strdup("0000000000000000");
	if (n == 0)
		return (str);
	i = 16;
	while (n > 0)
	{
		hex = ft_itobase(n % 16, "0123456789ABCDEF");
		n /= 16;
		str[--i] = *hex;
		free(hex);
	}
	return (str);
}
